package latihan_2;

class Mahasiswa {
    public int nrp;
    public String nama;
    
    public Mahasiswa(int nrp, String nama) {
        this.nrp = nrp;
        this.nama = nama;
    }
    public int getNrp() {
        return nrp;
    }
    public String getNama() {
        return nama;
    }
}
